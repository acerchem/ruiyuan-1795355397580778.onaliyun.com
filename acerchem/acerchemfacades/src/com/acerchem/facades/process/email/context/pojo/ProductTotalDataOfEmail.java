package com.acerchem.facades.process.email.context.pojo;

public class ProductTotalDataOfEmail {
	private String quantity;
	private String netWeight;
	private String grossWeight;
	
	private String packingWeight;
	private String packingCount;

	private String shippingMarks;
	private String poNo;
	
	
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getNetWeight() {
		return netWeight;
	}
	public void setNetWeight(String netWeight) {
		this.netWeight = netWeight;
	}
	public String getGrossWeight() {
		return grossWeight;
	}
	public void setGrossWeight(String grossWeight) {
		this.grossWeight = grossWeight;
	}
	public String getPackingWeight() {
		return packingWeight;
	}
	public void setPackingWeight(String packingWeight) {
		this.packingWeight = packingWeight;
	}
	public String getPackingCount() {
		return packingCount;
	}
	public void setPackingCount(String packingCount) {
		this.packingCount = packingCount;
	}
	public String getShippingMarks() {
		return shippingMarks;
	}
	public void setShippingMarks(String shippingMarks) {
		this.shippingMarks = shippingMarks;
	}
	public String getPoNo() {
		return poNo;
	}
	public void setPoNo(String poNo) {
		this.poNo = poNo;
	}
	
	
	
	
}
